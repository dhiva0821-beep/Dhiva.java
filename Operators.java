class Operators {
    public static void main (Stringex [] args){
        int u=5;
        int v=7;
        System.out.println(u&v);
        System.out.println(u|v);
        System.out.println(u^v);
        System.out.println(~u);
        System.out.println(~v);
        System.out.println(u<<v);
        System.out.println(u>>v);
        System.out.println(v>1 && v<20);
        System.out.println(v>1 || v<10);
        boolean fg = false;
        System.out.println(!fg) ;

    }
}
